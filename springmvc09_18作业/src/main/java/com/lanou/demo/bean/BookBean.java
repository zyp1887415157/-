package com.lanou.demo.bean;

public class BookBean {
    private String name;
    private int id;


    @Override
    public String toString() {
        return "BookBean{" +
                "name='" + name + '\'' +
                ", id=" + id +
                '}';
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
