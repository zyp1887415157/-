package com.lanou3g.homework;

public class Goods {

    private String name;
    private float price;

    public String getName() {
        return name;
    }

    public Goods setName(String name) {
        this.name = name;
        return this;
    }

    public float getPrice() {
        return price;
    }

    public Goods setPrice(float price) {
        this.price = price;
        return this;
    }
}
