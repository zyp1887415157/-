package com.lanou3g.homework;

public class Person {
    private String man;
    private String woman;
    private String child;
    private String name;




    public String getName() {
        return name;
    }

    public Person setName(String name) {
        this.name = name;
        return this;
    }

    public String getMan() {
        return man;
    }

    public Person setMan(String man) {
        this.man = man;
        return this;
    }

    public String getWoman() {
        return woman;
    }

    public Person setWoman(String woman) {
        this.woman = woman;
        return this;
    }

    public String getChild() {
        return child;
    }

    public Person setChild(String child) {
        this.child = child;
        return this;
    }
}
